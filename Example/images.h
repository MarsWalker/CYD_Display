/**
 * Searches for an image in the look-up table by its string identifier name.
 * Trims whitespace and normalizes text to lowercase to prevent sorting mismatches.
 * * @param imgName The lookup reference name of the requested icon.
 * @return The integer index in IMAGE_TABLE if found; otherwise -1.
 */
int FindImageIndex(String imgName)
{
    imgName.trim();
    imgName.toLowerCase();

    for (int i = 0; i < IMAGE_TABLE_SIZE; i++)
    {
        if (imgName == IMAGE_TABLE[i].name) { return i; }
    }
    return -1;
}

